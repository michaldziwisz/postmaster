import TelegramPresentationData
import UIKit

public enum ChatMessageUnlockMediaNodeVoiceOver {
    public struct Resolved: Equatable {
        public let label: String
        public let hint: String?
        public let traits: UIAccessibilityTraits
        
        public init(label: String, hint: String?, traits: UIAccessibilityTraits) {
            self.label = label
            self.hint = hint
            self.traits = traits
        }
    }
    
    public static func resolve(strings: PresentationStrings, amountString: String, isEnabled: Bool) -> Resolved {
        var traits: UIAccessibilityTraits = [.button]
        if !isEnabled {
            traits.insert(.notEnabled)
        }
        
        return Resolved(
            label: strings.Chat_PaidMedia_UnlockMedia("⭐️ \(amountString)").string,
            hint: nil,
            traits: traits
        )
    }
}

